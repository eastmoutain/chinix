#ifndef KERNEL_CFG_H
#define KERNEL_CFG_H

#define MEMBASE                 0x0000000000000000 // 0
#define KERNEL_BASE             0xffffffff80000000 // -2GM
#define KERNEL_LOAD_OFFSET      0x0000000000200000 // 2M
#define KERNEL_SPACE_BASE       0xffffff8000000000 // -512GB
#define KERNEL_SPACE_SIZE       0x0000008000000000 // 512G
#define USER_SPACE_BASE         0x0000000000000000
#define USER_SPACE_SIZE         0x0000800000000000

#define PHYS_LOAD_ADDR  (MEMBASE + KERNEL_LOAD_OFFSET) // 2M
#define PHYS_ADDR_DELTA (KERNEL_BASE + KERNEL_LOAD_OFFSET - PHYS_LOAD_ADDR)
#define PHYS(x) ((x) - PHYS_ADDR_DELTA)

#endif // KERNEL_CFG_H

