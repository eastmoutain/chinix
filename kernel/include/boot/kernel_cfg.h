#ifndef KERNEL_CFG_H
#define KERNEL_CFG_H

#define MEMBASE 0
#define KERNEL_BASE  0xffffffff80000000
#define KERNEL_LOAD_OFFSET  0x00200000
#define KERNEL_ASPACE_BASE  0xffffff8000000000UL // -512GB
#define KERNEL_ASPACE_SIZE  0x0000008000000000UL
#define USER_ASPACE_BASE    0x0000000000000000UL
#define USER_ASPACE_SIZE    0x0000800000000000UL

#define PHYS_LOAD_ADDRESS (MEMBASE + KERNEL_LOAD_OFFSET)
#define PHYS_ADDR_DELTA (KERNEL_BASE + KERNEL_LOAD_OFFSET - PHYS_LOAD_ADDRESS)
#define PHYS(x) ((x) - PHYS_ADDR_DELTA)

#endif // KERNEL_CFG_H

